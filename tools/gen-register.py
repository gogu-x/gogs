#!/usr/bin/env python3
# gen-register.py <go_out_dir> <proto_dir>
# 生成 codec/register.go，集中注册所有消息
import sys, re
from pathlib import Path

go_out    = Path(sys.argv[1]).resolve()
proto_dir = Path(sys.argv[2]).resolve()

# pkg_path -> [MessageName, ...]
from collections import defaultdict
groups = defaultdict(list)
pkg_paths = {}  # go_package -> import path

for proto in sorted(proto_dir.rglob("*.proto")):
    content = proto.read_text(encoding="utf-8")
    m = re.search(r'option\s+go_package\s*=\s*"([^"]+)"', content)
    if not m:
        continue
    go_pkg = m.group(1)
    pkg_paths[go_pkg] = go_pkg
    groups[go_pkg].extend(re.findall(r"message\s+(\w+)\s*{", content))

if not groups:
    sys.exit("no messages found")

# 生成 codec/register.go
out_path = go_out / "codec" / "register.go"

lines = ["// Code generated by gen-register.py. DO NOT EDIT.\n"]
lines.append("package codec\n\n")
lines.append("import (\n")
for go_pkg in sorted(groups):
    alias = go_pkg.rsplit("/", 1)[-1]
    lines.append(f'\t{alias} "{go_pkg}"\n')
lines.append(")\n\n")
lines.append("func init() {\n")
lines.append("\tRegisterMsg(\n")
for go_pkg in sorted(groups):
    alias = go_pkg.rsplit("/", 1)[-1]
    for name in groups[go_pkg]:
        lines.append(f"\t\t&{alias}.{name}{{}},\n")
lines.append("\t)\n}\n")

out_path.write_text("".join(lines), encoding="utf-8")
total = sum(len(v) for v in groups.values())
print(f"generated {out_path} ({total} messages, {len(groups)} packages)")
